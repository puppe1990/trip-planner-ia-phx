  def t(locale, key, bindings \\ %{}) do
    locale = TripPlannerIaWeb.Plugs.SetLocale.normalize_locale(locale)

    @translations
    |> Map.get(locale, @translations["pt-BR"])
    |> Map.get(key, key)
    |> interpolate(bindings)
  end

  defp interpolate(text, bindings) do
    Enum.reduce(bindings, text, fn {key, value}, acc ->
      String.replace(acc, "%{#{key}}", to_string(value))
    end)
  end
